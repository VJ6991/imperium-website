<h3>IMPROVEMENT AND CONSTRUCTION EXCHANGES</h3>

</P>The Build-To-Suit Exchange, also referred to as a construction or improvement exchange, gives the Exchanger the opportunity to use all or part of the exchange funds for construction (or improvement) of the replacement property and still accomplish a tax deferred exchange.  In the most common type of Build-To-Suit Exchange (Delayed Improvement Exchange) the Exchanger sells the relinquished property in a delayed exchange and then acquires the replacement property after it has been improved with the exchange funds from the relinquished property.  It is important to note that any improvements made to the replacement property after the Exchanger takes title are considered to be "goods and services" (are not like-kind to the relinquished property) and will result in taxable boot.  For this reason, escrow hold back accounts, prepaying for construction services or for building materials, etc. do not work for Build-To-Suit Exchanges.  Instead, the transaction is structured using the "safe harbor" guidelines provided by Revenue Procedure 2000-37 wherein the Build-To-Suit Exchange is accomplished using an Exchange Accommodation Titleholder (EAT) to hold title to the replacement property while the improvements are made.

</P>The structure of a delayed improvement exchange is the same as a delayed exchange except: the Qualified Intermediary (through the EAT) uses the exchange funds and/or loan funds to purchase the replacement property to be improved; and the EAT enters into a construction contact with a general contractor and a construction management agreement and disbursement agreement with the Exchanger.  As the construction is completed, the QI pays the invoices with exchange funds and on or before the 180th day (or tax filing deadline) the EAT transfers the improved property to the Exchanger.  Another difference relates to the identification statement.  During the 45-day Identification Period, the Exchanger must submit to the QI an unambiguous description of the land (or existing property) and the improvements to be constructed.  The 1031 regulations require that the improvements be described "in as much detail as is practicable at the time of the identification."  For more information and diagrams relating to Build-To-Suit exchanges,  click <a href="https://www.ipx1031.com/?s=build+to+suit&x=0&y=0">here</a>.

