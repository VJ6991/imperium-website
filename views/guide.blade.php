<h3>WHAT IS A TAX DEFERRED EXCHANGE?</h3>

</P>A tax-deferred exchange represents a simple, strategic method for selling one qualifying property and the subsequent acquisition of another qualifying property within a specific time frame.

</P>Although the logistics of selling one property and buying another are virtually identical to any standard sale and purchase scenario, an exchange is different because the entire transaction is memorialized as an exchange and not a sale. It is this distinction between exchanging and not simply selling and buying, which ultimately allows the taxpayer to qualify for deferred gain treatment.

</P>



<h3>INTERNAL REVENUE CODE, SECTION 1031</h3>

</P>Because exchanging represents an IRS recognized approach to the deferral of capital gain taxes, it is important for us to appreciate the components and intent underlying such a tax deferred or tax free transaction. It is within Section 1031 of the Internal Revenue Code that we find the core essentials necessary for a successful exchange. Additionally, it is within the regulations, issued by the Treasury Department in 1991, that we find generally accepted standards and rules for completing a qualifying transaction.

</P><h3>WHY EXCHANGE?</h3>

</P>Any investor who expects to acquire replacement property subsequent to the sale of his existing property should consider an exchange. To do otherwise would necessitate the payment of capital gain taxes in amounts which can exceed 20 to 30%, depending on the appropriate combined federal and state tax rates. To put it another way, the investor who sells, pays taxes and reinvests the after tax proceeds has 70 to 80% of the money to reinvest than the investor who completes a 1031 Exchange.  





