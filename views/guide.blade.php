<h3>REVERSE EXCHANGES</h3>

</P>A reverse exchange is useful when an Exchanger wants to acquire the replacement property first and dispose of the relinquished property later on a tax deferred basis.  A reverse exchange may be appropriate if the relinquished property falls out of escrow or the replacement property must close before the relinquished property is sold or ready to close.  To have a valid exchange, one of the properties must be "parked" to prevent the Exchanger from being in title to the relinquished property at the same time they are in title to the replacement property.

</P>

<b>Replacement Property Parked Reverse Exchange</b>

</P>In this type of reverse exchange, the replacement property is "parked" with an entity called an Exchange Accommodation Titleholder (EAT).  This is the most common type of reverse exchange and is sometimes known as the "Exchange Last Method".  In Phase 1 of a Replacement Property Parked Exchange, the money needed to buy the replacement property is loaned to the EAT by the Exchanger or a 3rd party lender.  After receiving the money, the EAT acquires the replacement property from the Seller and holds title to the property for a maximum of 180 calendar days.  In Phase 2, a buyer is found and the relinquished property is transferred to that Buyer; the net sale proceeds are transferred to the EAT (through the Qualified Intermediary); the EAT pays off (or pays down) the loan on the replacement property; and transfers that property to the Exchanger in completion of the exchange.  The process is summarized in Diagram 3:



