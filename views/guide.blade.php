<h3>LIKE KIND PROPERTY DEFINED</h3>

</P>The relinquished property and replacement property must be like-kind to qualify for exchange treatment under section 1031.  The term like-kind refers to the nature or character of the property not its grade or quality.  For this reason nearly all real property is like-kind to each other.  The following items are all considered like-kind and can be exchanged for each other: vacant land, commercial buildings (office, industrial or warehouse), ranches, farms, rental homes, apartment buildings, tenant-in-common interests as well as leases of 30 years or more (including options).



</P>Personal property which is held for investment or business use is also eligible for a 1031 Exchange and can be exchanged for property of like-kind.  However, the like-kind property standard has been interpreted more narrowly in the case of exchanges of personal property than in exchanges of real property.  For example, while undeveloped land may be exchanged for an office building, a car may not be exchanged for a truck.  Under the Regulations for personal property, depreciable tangible personal property which is being held for productive use in a trade or business is considered to be exchanged for like-kind property if it is exchanged for property that is either like-kind or like class.  



</P>Although the like-kind standard is narrower for personal property exchanges than real property exchanges, personal property exchanges are important especially in transactions involving farms or ranches.  Depreciable tangible personal property is like class if it is within the same "General Asset Class" or within the same "Product Class."  This is a safe harbor which is provided by the 1031 regulations.  The "safe harbor" means that if two items are in the same General Asset Class or Product Class, they are considered to be "like-kind" for a 1031 Exchange; this fact will not be challenged by the IRS in the event of an audit of the exchange.  



</P>Property within a General Asset Class consists of depreciable tangible personal property described in Revenue Procedure 87-56.  The General Asset Classes are as follows:

</P>

<li>1)	Office furniture, fixtures and equipment (asset class 00.11);</li>

<li>2)	Information systems (computers and peripheral equipment) (00.12);</li>

<li>3)	Data-handling equipment, except computers (00.13);</li>

<li>4)	Airplanes and helicopters (air frames and engines), except those used in commercial or contract carrying of passengers or freight (00.21);</li>

<li>5)	Automobiles and taxis (00.22);</li>

<li>6)	Buses (00.23);</li>

<li>7)	Light general-purpose trucks (00.241);</li>

<li>8)	Heavy general-purpose trucks (00.242);</li>

<li>9)	Railroad cars and locomotives, except those owned by rail road transportation companies (00.25);</li>

<li>10)	Tractor units for use over the road (00.26);</li>

<li>11)	Trailer and trailer-mounted containers (00.27);</li>

<li>12)	Vessels, barges, tugs and similar water transportation equipment, except those used in marine construction (00.28); and</li>

<li>13)	 Industrial steam and electric generation and/or distribution systems (00.4).</li>

</P>The other way to determine if two items are like-class is found in the six digit product classes within Sectors 31, 32 and 33 (pertaining to manufacturing industries) of the North American Industry Classification System (NAICS).

</P>

<h3>TIME REQUIREMENTS</h3>

</P>The Identification Period in a 1031 Exchange begins on the date the Exchanger transfers the "benefits and burdens of ownership" of the first relinquished property and ends at midnight on the 45th day thereafter.  The Exchange Period begins on the same day as the Identification Period (the date the Exchanger transfers the benefits and burdens of ownership of the relinquished property) and ends at midnight on the earlier of, the 180th day thereafter or the due date (determined with regard to extension), for the Exchanger's tax return for the taxable year in which the transfer of the relinquished property occurred.



</P>It should be noted that the counting of the 45 and 180 periods begins on the day after the first relinquished property is transferred not on the day of transfer itself.  Accordingly, the Identification and Exchange Periods are a full 45 and 180 days, respectively.  As such, the total Exchange Period is 180 days and the 45 day Identification Period is included in that period; it is not 45 days plus 180 days as many individuals believe.  It should also be noted that the time periods are calendar days, not business days.  Accordingly, the Identification and Exchange Periods will count weekends and holidays.  If the 45th day or the 180th day falls on a weekend or holiday it will NOT be extended until the next business day; for that reason, Exchangers need to be aware of the expiration of the Identification and Exchange Periods and plan accordingly.  The Exchanger must receive the "benefits and burdens of ownership" of the replacement property by midnight of the end of the Exchange Period.

</P><b>Tax Season Issues</b>

</P>When the relinquished property is transferred after October 17th (October 18th when the tax return is due in a leap year), less than 180 days will be available to  close on the replacement property for a taxpayer whose tax return is due April 15th of the following year.  The Exchanger can have the full 180 days for the Exchange Period by filing an extension of the tax return.  Similarly, a corporation with a due date of March 15th will have less than 180 days if the closing of its relinquished property is on or after similar dates in September unless a filing extension is requested.  



</P>The easiest way to remember these tax season issues is that the transfer of the relinquished property and the transfer of the replacement property must be reported in one tax return.  This is why the IRS allows the Exchanger to file for an extension to provide a full 180 day Exchange Period regardless of what time of year they transfer the relinquished property. 

</P>

<h3>SELECTING A QUALIFIED INTERMEDIARY </h3>

</P>Security of the funds that are being held by the Qualified Intermediary (QI) for up to 180 days, is essential to making sure they are available to acquire the replacement property and complete the exchange.  When selecting QI, it is very important to consider a number of factors including: how the exchange funds are being held (are they in segregated accounts or are they comingled); the financial strength of the QI well as the soundness of its business practices (are they likely to remain in business for the exchange period and could they withstand a financial loss that could occur as a result of a cybercrime); whether it complies with the statute and IRS regulations (to be within the "safe harbor" given by the IRS); and whether its employees are knowledgeable and competent (to assist you and your client).  

</P>

With regard to security, the Exchanger should consider the following factors: 

</P>

<li>	whether there exists a performance guarantee (for the QI) from a financially stable third party; </li>	

<li>		whether it has a significant Fidelity Bond (crime insurance); </li>	

<li>	whether it has a significant amount of Errors & Omission Insurance; and </li>	

<li>	how the exchange funds will be held and/or invested. </li>	

</P>

With regard to financial strength and the possession of sound business practices, the Exchanger should determine:

</P>

<li>	<i>if the QI undergoes 3rd party audits;</i> </li>	

<li>		<i>whether the QI requires dual signatures on disbursements of exchange funds; and</i> </li>	

<li>	<i>whether the QI has a procedure that requires a written authorization by the Exchanger before exchange funds can be disbursed.</i>  </li>	

</P>

Finally, it is important to know whether the Qualified Intermediary has attorneys, accountants and/or Certified Exchange Specialists (CES®) on its staff knowledgeable with the regulations and legal requirements for a 1031 Exchange.

</P>

<b>Qualified Intermediary is an Independent Third Party</b>

</P>A requirement to having a valid 1031 Exchange is that the Exchanger must avoid having actual or constructive receipt of the exchange funds.  One way to accomplish this is to retain a QI to satisfy the "safe harbor" requirements under Treasury Regulation 1.1031(k)-1(g)(4).  The QI must not be a "disqualified person" as defined in the 1031 regulations.  Examples of parties disqualified to serve as a QI include parties related to the exchanger which are defined in sections 267(b) and 707(b)(1) of the Internal Revenue Code (e.g. ancestors, lineal descendants, siblings, spouses and related entities) as well as agents of the Exchanger.  

</P>

For purposes of the regulations, a person who has acted as the Exchanger's employee, attorney, accountant, investment banker or broker, or real estate agent or broker within the two year period prior to the beginning of the exchange is disqualified to serve as the QI.  There is an exception for the performance of routine financial, title insurance, escrow or trust services by a financial institution, title insurance company or escrow company or for services solely related to facilitating a 1031 Exchange.  Accordingly, an Exchanger can use the same QI repeatedly.

</P>

As a result when selecting a QI, consideration should not only be given to the service and security they offer but also whether they are considered to be a "disqualified party, especially if the Exchanger would like to have their exchange facilitated by their attorney or accountant. 

</P>

<h3>A FEW COMMON EXCHANGE QUESTIONS AND ANSWERS</h3>

</P><b>Equity and Capital Gain</b>

</P>Is my tax based on my equity or my capital gain?

</P>Tax is calculated upon the capital gain. Gain and Equity are two separate and distinct items. Equity is calculated by subtracting the net loan balance from the net sales proceeds.  The resulting figure has nothing to do with the amount of potential taxes owed.  Capital Gain is calculated by subtracting the adjusted basis of the property (original purchase price minus depreciation taken plus the cost of any improvements) from the net sale proceeds.  It is this figure that represents the amount realized from the sale that is potentially taxable.  This is also known as the 'realized gain".  The amount of the realized gain that is subject to tax is called the "recognized gain".  In a taxable sale (no 1031 Exchange) the realized gain is all recognized.  In a fully deferred 1031 Exchange, no gain is recognized; the realized gain is deferred.

</P><b>Property Conversion</b>

</P>How long must I wait before I can convert an investment property into my personal residence?

</P>In March 2008, the IRS issued Revenue Procedure 2008-16.  This provided guidance how a second home or vacation home could be made eligible for a 1031 Exchange.  In the Revenue Procedure, the IRS created safe harbors under which it will not challenge whether a second or vacation home that is either relinquished or replacement property in a 1031 Exchange qualifies as property held for use in a trade or business or for investment purposes.

</P>

The safe harbor for a second or vacation home to qualify as relinquished property in a 1031 Exchange requires the Exchanger to have owned it for at least two years before the exchange.  In addition, within each year immediately before the exchange, the Exchanger must have 1) rented it at fair market rental for 14 or more days; and 2) restricted personal use to no more than 14 days or 10% of the number of days it was actually rented (whichever is more) within each of the two years before the exchange.

</P>

In addition, the safe harbor also applies to a second or vacation home as a replacement property.  To qualify, the Revenue Procedure requires the Exchanger to own the second or vacation home for two years after the exchange.  In addition, for each of those two years, the Exchanger must 1) rent the unit at fair market rental for 14 or more days; and 2) restrict personal use to 14 days or 10% of the number of days it was actually rented (whichever is more) during each of the two years after the exchange.  After that, the Exchanger could move into the property as his or her primary residence or solely use it for personal purposes as a vacation property. 

</P>

Accordingly, Revenue Procedure 2008-16 provides another "exit strategy" from investment real estate.  An investor can sell any type of investment property (an office building, a farm or ranch, an apartment building, etc.) and exchange into a future personal use property (primary residence or a vacation home).  As long as the Exchanger complies with the requirements of Revenue Procedure 2008-16, the IRS will not challenge whether the dwelling is "Qualified Property". If the Exchanger fails to comply with the safe harbor after the purchase, then there is an affirmative obligation to amend the income tax return for the year the exchange was reported.

</P>

<b>Involuntary Conversion</b>

</P>What if my property was involuntarily converted by a disaster or I was required to sell due to a governmental or Eminent Domain action?

</P>Involuntary conversion is addressed within Section 1033 of the Internal Revenue Code.  The 1033 exchange involves the "involuntary" conversion of property, a situation frequently caused by the government's power of eminent domain.  Eminent domain is defined as "the power of a government to take private property for public use, usually with compensation paid to the owner".  Property owners have limited rights when facing the threat of their property being taken via "eminent domain".

</P>The treatment of capital gains tax associated with a property that is appropriated by the government via eminent domain is a subject investment property owners need to be aware of. Section 1033 of the Internal Revenue Code addresses how an investor/business owner can defer payment of capital gains taxes when participating in an involuntary conversion of their investment or business property.  This process is alternatively called a 1033 Exchange or a 1033 Rollover.  The most common qualifier for a 1033 Exchange is seizure due to eminent domain.  Other circumstances that make a property eligible for a 1033 Exchange are:

</P>

<li>	Destruction of the property that is beyond the control of the taxpayer (fires, severe storms, floods, etc.); </li>

<li>Theft - the criminal appropriation of property by another (swindling, false pretenses, etc.); and</li>

<li>Taking of the property through seizure (contraband and the fruits and instrumentalities of crime).</li>

</P>Taxpayers must replace their 1033 property within the period beginning with the involuntary conversion and ending two (2) years (three (3) years if condemned real property) after the close of the first taxable year in which any part of the gain is realized.  Notification of replacement must be included in the owner's tax return for the taxable year or years in which replacement occurs in order to avoid keeping the period for assessment open.  The notification must set forth all the details in connection with the investment.

</P>The Replacement Property

</P>Rules for choosing a replacement property set forth in a 1033 Exchange are very specific.  The kind of replacement property is narrower than under a 1031 Exchange. The general requirement is that the replacement property must be similar or related in service or use (similar-use requirement).  This has been defined to mean property which is functionally similar and has the same uses as the converted property.  

Section 1033 allows 2 to 3 years to choose the replacement property.  Condemned real estate held for business or investment can be replaced by property held either for productive use in a trade or business or for investment (like-kind standard), which is less restrictive than the similar-use requirement for other involuntary conversions.  Other condemned real estate (such as primary residences or second homes) would be subject to the more rigid similar-use requirement. The taxpayer must intend and document that the acquired real estate serve as the replacement for the condemned real estate. The replacement requirement can also be satisfied through construction of a replacement property on land already owned by the taxpayer.  Funds from any source (including the condemnation) can be used to construct the replacement property.  

</P>

