</P><b>Partnership or Partial Interests  </b>

</P>If I am an owner of investment property in conjunction with others, may I exchange only my partial interest in the property?

</P>Yes, if you own and report taxes as tenants in common. However, if your interest is not in the property but actually an interest in the partnership which owns the property, your exchange will not qualify. This is because partnership interests are excepted from deferral treatment by Section 1031(a)(2)(D). But don't be confused! If the entire partnership desires to stay together and exchange their property for a replacement, that would qualify.

