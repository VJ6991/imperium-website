<h3>MISCONCEPTIONS ABOUT EXCHANGING</h3>

</P>Before we continue discussing the various types of exchange structures and their associated rules, let´s identify four common exchange misconceptions:

</P>All exchanges must involve the swapping or trading with other property owners.  (NO)

</P>Before the case of Starker v. U.S. in 1979, and the codification of delayed exchanges in 1984, exchange transactions required the actual swapping of deeds and simultaneous closing among all parties to successfully complete a 1031 Exchange. Often these exchanges were comprised of multiple exchanging parties as well as numerous exchange properties. However since 1984 and the issuance of the 1031 regulations in 1991, there is no such requirement to swap your property with someone else in order to complete a valid 1031 Exchange. The regulations make the process similar to a standard sale and purchase of property.

</P>All exchanges must close simultaneously.  (NO)

</P>As discussed above, there was a time when all exchanges had to be closed on a simultaneous basis, however, exchanges are usually not completed in this format any longer. A majority of exchanges are now closed as delayed or deferred exchanges.

</P>Like-kind means purchasing the same type of property which was sold.  (NO)

</P>The definition of like-kind has often been misinterpreted to mean that the property being acquired must be utilized in the same form as was the property being exchanged. In other words, apartments for apartments, hotels for hotels, farms for farms, etc. However, the term like-kind refers to the nature or character of property not its grade or quality.  For this reason nearly all real property is like-kind to each other.  The like-kind standard has been interpreted more narrowly in the case of exchanges of personal property held for investment or business use.  Under the 1031 regulations for personal property, depreciable tangible personal property which is held for productive use in a trade or business is considered like-kind if exchanged for property that is either like-kind or like class.  For more information about like-kind property (including examples), please click <a href="https://www.ipx1031.com/faq-like-kind-property/">here</a>. 

</P>Exchanges must be limited to one relinquished property and one replacement property.  (NO)

</P>This is another exchanging myth. There are no restrictions on the number of relinquished properties.  However there are restrictions on the number of replacement properties which can be identified.  For more information on the identification rules in a 1031 Exchange please click <a href="https://www.ipx1031.com/deadlines-and-identification-requirements/">here</a>.

</P>

<h3>PARTIES TO AN EXCHANGE</h3>

</P>There are four parties involved in a delayed or deferred 1031 Exchange.

</P>In Phase One (the sale of your relinquished property), they are: the Taxpayer (also called the Exchanger), the Buyer or Purchaser, and the Qualified Intermediary (also called the facilitator).

</P>In Phase Two (the purchase of your replacement property), they are: the Taxpayer (also called the Exchanger), the Seller, and the Qualified Intermediary (also called the facilitator).  The process is explained in more detail below in the section entitled "Deferred or Delayed Exchanges".

</P>

<h3>BASIC EXCHANGE RULES - DEFERRING ALL CAPITAL GAIN</h3>

</P>Let us look at a basic concept, which applies to all exchanges. Utilize this concept to fully defer the capital gain taxes realized from the sale of a relinquished property.  If you acquire replacement property that has:

</P>1)	A purchase price greater or equal to the net sales price of the relinquished property (in other words you buy equal or up in value),

</P>2)	Net equity greater or equal to the equity in the relinquished property (in other words you move all the equity from your relinquished property to the replacement property), and you,

</P>3)	Replace the value of the debt that was on the relinquished property.  This can be achieved by placing a loan on the replacement property equal in value to the loan that existed on the relinquished property, adding cash from outside of the 1031 Exchange or a combination of the two.

</P>If you satisfy all three of these items you will have a fully tax deferred exchange.  If you do not, your 1031 Exchange is still valid, however you will pay taxes on the "boot" (non like-kind property) received.  The gain subject to tax is the lesser of the boot received or the realized gain.  In other words, realized gain serves as the ceiling on the amount of gain that can be taxed.

</P>

