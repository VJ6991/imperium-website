<h3>BUSINESS ASSETS OR PERSONAL PROPERTY EXCHANGES</h3>

</P>Although our discussion in this tutorial primarily involves the exchange of real property, Internal Revenue Code Section 1031 allows the exchange of many types of property other than real estate. Businesses may exchange, for example, rail cars, trucks, ships or livestock, as well as investors may exchange collectibles such as artwork, collector cars or gold bullion, etc.

</P>While the basic exchange rules are the same, as mentioned above, the definition of like-kind is more restrictive than it is for real estate exchanges. Some personal property and business items are not exchangeable. Most notable in this group are items such as goodwill or inventory.  It is very important that an Exchanger conducting a personal property exchange seek the assistance of their tax advisor with respect to the identification of like-kind property as well as assistance in structuring the exchange to maximize tax deferral.  For more information on personal property exchanges, click <a href="https://www.ipx1031.com/our-services/personal-property-exchanges/">here</a>.

